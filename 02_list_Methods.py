list1=[10,20,30,40]
list1.append([50,60]) # List ke end me ek element add karta hai.
print(list1)
list1.insert(2,15)  # specified index pr element add karta hai.
print(list1)
list1.remove(20)  # List me se first occurrence of x ko delete karta hai.
print(list1)
list1.pop(2)   # specified index pr se element ko delete karta hai.
print(list1)
list2=[9,8,7,6,5,4,3,2,1]
list2.reverse()   # List ke elements ko ulta kar deta hai.
print(list2)
list2.sort()  # List ko ascending order me sort karta hai.
print(list2)
list3=[28,96,87,99,57]
list3.sort(reverse=True) # List ko descending order me sort karta hai.
print(list3)


 